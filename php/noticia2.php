						<center><h3>Inicio de Actividades Conjuntas PDVSA FENIX IUTET.</h3></center>
						<h4>
						 El pasado día 19 de octubre, se dio la primera visita guiada en las instalaciones de PDVSA OCCIDENTE por parte de un nutrido 
						 grupo de estudiantes pertenecientes a las diferentes carreras dictadas por el Instituto Universitario de Tecnología del Estado Trujillo
						  (IUTET), siendo la misma el inicio de una relación interinstitucional que permitirá la formación y el intercambio de saberes relacionados
						   con la Gestión del Riesgo.</h4><br>
						<h4>La actividad fue concertada entre el Ing. José Javier Leal, enlace del IUTET para la creación del PNF en Gestión de Riesgos y Administración 
						de Desastres adscrito a la Sub Dirección Académica de esta institución, y el TSU Denny Avendaño Líder de la Unidad de Prevención y Control 
						de Emergencias Naturales, la cual se encuentra bajo la dirección del ING. PEDRO DIAZ GERENTE DE SEGURIDAD INDUSTRIAL E HIGIENE OCUPACIONAL 
						PDVSA, DIRECCIÓN EJECUTIVA OCCIDENTE encargándose esta de preparar a la población para enfrentar y minimizar el impacto que desde el punto 
						de vista humano y material pudiera ocasionar cualquier evento natural, tal como una inundación o terremoto, que alcanzase ocasionar una falla
						 incontrolable en el Sistema de Protección Costanera del Plan de Contingencia para la Costa Oriental del Lago de Maracaibo (Plan COLM), 
						 conduciendo para ello diversos programas técnicos y educativos en las áreas operacionales y población en general.</h4><br>

						<h4>En ese contexto el equipo de trabajo del TSU Denny Avendaño, conjuntamente con el Sr. Walter Homberger y el Ing. Roberto Kissner, entre otros
						 miembros de su equipo, desarrollaron una Presentación Institucional de la Unidad “Prevención Control Emergencia Naturales” para los estudiantes,
						  conduciendo luego una visita de reconocimiento al Sistema de Protección Costanera mejor conocido como "Dique o Muro de Contención", finalizando 
						  con una práctica de control de incendios industriales en el Centro de Adiestramiento Bomberil CABULE.</h4>

						<h4>Este tipo de trabajo conjunto permitirá al IUTET fortalecer el Proyecto para la Creación del PNF en Gestión de Riesgos y Manejo de Desastres, 
						entregado el pasado mes de enero ante el Ministerio del Poder Popular para la Educación Universitaria así como coadyuvar al desarrollo de las 
						“TROPAS ALIADAS” de la Nueva PDVSA con Visión Nacional, Popular y Revolucionaria para la Atención Comunitaria en el Occidente del País.</h4>
