						<center><h3>Estudiantes de la Misión Sucre reciben Taller de Gestión de Riesgo</h3></center>
						<h4><br>
						 Un nutrido grupo de estudiantes, en su mayoría damas, integrantes de las menciones Educación Inicial, Integral y Especial del sexto 
						 semestre en la “Aldea Ciudad de Valera”, participaron conjuntamente con sus Profesoras Coordinadoras, Melida David y Luisana Álvarez,
						  en el Taller de “Gestión de Riesgos Socionaturales” que viene siendo dictado por el personal de la Unidad de Prevención y Control 
						  de Emergencias Naturales adscrita a la Gerencia de Seguridad Industrial e Higiene Ocupacional de PDVSA OCCIDENTE, en fecha 24 de 
						  febrero del presente año.</h4><br>
						<h4>
						Esta actividad, forma parte del Plan de Formación Integral para todos los estudiantes Universitarios del Estado Trujillo, en el contexto
						 de la Creación, Desarrollo y Consolidación del Programa Nacional de Formación en Gestión de Riesgos y Administración de Desastres a cargo
						  del Profesor del IUTET Ingeniero Javier Leal, quien coordina el mismo a nivel nacional como enlace ante la Universidad Experimental de 
						  la Seguridad, el Ministerio del Poder Popular para la Educación Universitaria y las demás instituciones asociadas al mismo, bajo el 
						  espíritu del “Plan Nacional de Desarrollo Económico y Social Simón Bolívar 2007-2013” y la “Misión Alma Mater”.</h4><br>
						<h4>
						Las participantes, entre la variedad de tópicos tratados, manejaron aquellos relacionados con las Amenazas Naturales, como fenómeno natural, 
						que pueden ocasionar daños: muerte, lesiones u otros impactos a la salud, al igual que daños a la propiedad, la pérdida de medios de sustento 
						y de servicios, trastornos sociales y económicos, o daños ambientales, y su relación con las Vulnerabilidades comprendidas en las 
						características y las circunstancias de una comunidad, sistema o bien que los hacen susceptibles a los efectos dañinos de una amenaza, dando 
						lugar a los DESASTRES como UN RIESGO MAL MANEJADO, de los cuales ya se han tenido fuertes experiencias en el territorio trujillano.</h4>
